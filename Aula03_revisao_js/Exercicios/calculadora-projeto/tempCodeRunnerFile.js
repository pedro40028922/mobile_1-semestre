function soma(n1, n2) {
    let resultado = n1 + n2
    return resultado
}
function subtracao(n1, n2) {
    let resultado = n1 - n2
    return resultado
    
}
function divisao(n1, n2) {
    let resultado = n1 / n2
    return resultado
    
}
function multiplicacao(n1, n2) {
    let resultado = n1 * n2
    return resultado

}

